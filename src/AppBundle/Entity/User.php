    public function generateToken() {
        $length = 16;
        $symbols = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ`~!@#$%^&*()_+|\=-{}[]:;"\',.<>?/';
        $symbolsLength = strlen($symbols);
        $token = '';
        for ($i = 0; $i < $length; $i++) {
            $token .= $symbols[rand(0, $symbolsLength - 1)];
        }
        return $token;
    }

